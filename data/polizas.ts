export interface Poliza {
  id: string
  clienteId: string
  companiaId: string
  ramo: "autos" | "vida" | "gastos-medicos" | "empresa" | "hogar" | "flotilla"
  numeroPoliza: string
  vigenciaInicio: string
  vigenciaFin: string
  prima: number
  formaPago: "mensual" | "trimestral" | "semestral" | "anual"
  estatus: "activa" | "por-renovar" | "vencida" | "cancelada" | "gracia" | "rehabilitada"
  folios?: string[]
  tramites?: number
  // Campos para indicadores de cobranza
  primaEmitida: number // Prima total emitida
  primaCobrada: number // Prima total cobrada
  fechaEmision: string // Fecha de emisión
  periodoGracia?: string // Fecha fin del período de gracia (30 días)
  cancelacionMotivo?: "falta-pago" | "cliente" | "otro"
  rehabilitacionFecha?: string // Fecha de rehabilitación si aplica
  agente?: string // ID del agente que cobra
  // Campos adicionales para pólizas pendientes de pago
  incisoEndoso?: string // Inciso o Endoso
  nombreAsegurado?: string // Nombre del asegurado (puede ser diferente al cliente)
  ultimoDiaPago?: string // Último día de pago
  numeroRecibo?: string // # Recibo (ej: 1/1, 2/4)
  primaTotalRecibo?: number // Prima total del recibo
  registroSistemaCobranza?: boolean // Si está en sistema de cobranza
  fechasRecordatorio?: { // Fechas de recordatorio
    fecha1?: string
    fecha2?: string
    fecha3?: string
  }
  comentarios?: string // Comentarios adicionales
  notas?: string // Notas sobre recibos del cliente
  marcaActualizacion?: boolean // Marca por actualización
}

